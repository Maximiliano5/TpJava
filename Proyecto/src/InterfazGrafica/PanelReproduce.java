    public PanelReproduce(ArrayList<Publicacion> Public) {
        //initComponents();
        Public.addAll(Public);
        
        X=0;
        action = new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {
                X = X + 1;
               jProgressBar1.setValue(X);
            }
        };
        int i;
        for(i=0; i<Public.size(); i++){
           if (Public.get(i) instanceof Imagen) {
               jLabel1.setText("5:00");
               timer = new Timer(50,action);
           } else if (Public.get(i) instanceof Video) {
               jLabel1.setText("00:"+String.valueOf(((Video)Public.get(i)).getDuracion()));
               timer = new Timer(((Video)(Public.get(i))).getDuracion()*10,action);
           } else {
               jLabel1.setText("00:"+String.valueOf(((Audio)Public.get(i)).getDuracion()));
               timer = new Timer(((Audio)(Public.get(i))).getDuracion()*10,action);
           }
        
           jLabel9.setText(Public.get(i).getFecha());
           jLabel10.setText(Public.get(i).getNombre());
           jLabel11.setText(String.valueOf(Public.get(i).getCantMG()));
           jLabel12.setText(String.valueOf(Public.get(i).getComentarios()));
           jLabel13.setText(String.valueOf(Public.get(i).getEtiquetas()));
           jLabel14.setText(String.valueOf(Public.get(i).muestra())); 
           timer.start(); 
        }   
    }

